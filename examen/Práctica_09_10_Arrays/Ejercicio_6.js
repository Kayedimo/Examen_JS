// Declaramos el array original con los días de la semana
const semana = [ "lunes", "martes", "miércoles", "jueves", "viernes", "sábado", "domingo" ];

// Mostramos el array original en pantalla
document.write(semana, "<br>");  // lunes,martes,miércoles,jueves,viernes,sábado,domingo

// Proceso: usamos el método reverse() para invertir el orden del array
semana.reverse();

// Mostramos el array invertido
document.write(semana);  // domingo,sábado,viernes,jueves,miércoles,martes,lunes